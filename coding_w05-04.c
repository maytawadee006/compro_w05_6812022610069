#include <stdio.h>          // เรียกใช้ฟังก์ชันพื้นฐาน เช่น printf และ scanf

int main() {
// ประกาศตัวแปรเพื่อเก็บข้อมูลของผู้ใช้ 3 คน
char name[3];               // ชื่อเล่น (ตัวอักษร 1 ตัว) ของแต่ละคน
int age[3];                 // อายุ (จำนวนเต็ม) ของแต่ละคน
float height[3];            // ส่วนสูง (ทศนิยม 1 ตำแหน่ง) ของแต่ละคน
float weight[3];            // น้ำหนัก (ทศนิยม 1 ตำแหน่ง) ของแต่ละคน
char grade[3];              // เกรด (ตัวอักษร 1 ตัว) ของแต่ละคน

// รับข้อมูลจากผู้ใช้จำนวน 3 คน โดยรับข้อมูลต่อเนื่องใน 1 บรรทัด (1 คน)
for (int i = 0; i < 3; i++) {
printf("ป้อนข้อมูลของคนที่ %d (ชื่อ อายุ ส่วนสูง น้ำหนัก เกรด): ", i + 1);

                            
scanf(" %s %d %f %f %c", &name[i], &age[i], &height[i], &weight[i], &grade[i]); // รับค่าจากผู้ใช้: ชื่อ อายุ ส่วนสูง น้ำหนัก เกรด
                                                                                // %s: ตัวอักษร, %d: จำนวนเต็ม, %f: ทศนิยม
                                                                                // ช่องว่างหน้า %s ป้องกันการอ่าน Enter จากบรรทัดก่อน
}

// แสดงผลแบบตาราง จัดรูปแบบให้ตรงตำแหน่ง สวยงามตามตัวอย่าง
printf("\n+-------+-----+------------+-----------+-------------+\n");
printf("| %-5s | %-3s | %-10s | %-9s | %-11s|\n",
"Name", "Age", "Height(cm)", "Weight(kg)", "Grade Code");
printf("+-------+-----+------------+-----------+-------------+\n");

// แสดงผลข้อมูลของผู้ใช้ทั้ง 3 คน แบบจัดตำแหน่ง
for (int i = 0; i < 3; i++) {
printf("| %-5c | %-3d | %-10.1f | %-9.1f | %-11c |\n",
name[i], age[i], height[i], weight[i], grade[i]);
}

printf("+-------+-----+------------+-----------+-------------+\n");

return 0; // จบการทำงานของโปรแกรม
}          
